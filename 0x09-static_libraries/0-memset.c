#include <stdio.h>

/**
 * _memset - function to copy the number
 * @s: entry and return
 * @b: character to copy
 * @n: number of Bytes
 *
 * Return: The result and returns S
 */
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int a;

	for (a = 0; a < n ; a++)
	{
		s[a] = b;
	}
	return (s);
}
